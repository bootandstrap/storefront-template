#!/usr/bin/env node

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, '..')

const CANONICAL_TYPES_RELATIVE_PATH = 'src/types/supabase.ts'
const TARGET_TYPES_RELATIVE_PATH = 'apps/storefront/src/lib/supabase/database.types.ts'
const CANONICAL_MIGRATIONS_RELATIVE_DIR = 'supabase/migrations'
const TARGET_SCHEMA_VERSION_RELATIVE_PATH = 'apps/storefront/src/lib/supabase/schema-version.ts'
const DATED_MIGRATION_PATTERN = /^\d{8,14}_.+\.sql$/

const args = process.argv.slice(2).filter((arg) => arg !== '--')
const checkOnly = args.includes('--check')

function readFlag(name) {
  const index = args.indexOf(name)
  if (index === -1) {
    return null
  }

  return args[index + 1] ?? null
}

function resolveSourcePath() {
  const directSource = readFlag('--source-file') ?? process.env.BOOTANDSTRAP_WEB_SUPABASE_TYPES_SOURCE
  if (directSource) {
    return path.resolve(directSource)
  }

  const sourceRoot = readFlag('--source-root') ?? process.env.BOOTANDSTRAP_WEB_ROOT
  const candidateRoots = [
    sourceRoot,
    path.join(repoRoot, 'BOOTANDSTRAP_WEB'),
  ].filter(Boolean)

  for (const root of candidateRoots) {
    const candidatePath = path.join(path.resolve(root), CANONICAL_TYPES_RELATIVE_PATH)
    if (fs.existsSync(candidatePath)) {
      return candidatePath
    }
  }

  throw new Error(
    [
      'Unable to resolve canonical Supabase types source.',
      `Expected ${CANONICAL_TYPES_RELATIVE_PATH} under --source-root / BOOTANDSTRAP_WEB_ROOT,`,
      `or a direct file path via --source-file / BOOTANDSTRAP_WEB_SUPABASE_TYPES_SOURCE.`,
    ].join(' ')
  )
}

function normalize(content) {
  return content.replace(/\r\n/g, '\n')
}

function getCanonicalRepoRoot(sourcePath) {
  return path.dirname(path.dirname(path.dirname(sourcePath)))
}

function resolveSchemaVersion(sourcePath) {
  const canonicalRepoRoot = getCanonicalRepoRoot(sourcePath)
  const migrationsDir = path.join(canonicalRepoRoot, CANONICAL_MIGRATIONS_RELATIVE_DIR)
  const latestMigration = fs.readdirSync(migrationsDir)
    .filter((fileName) => DATED_MIGRATION_PATTERN.test(fileName))
    .sort()
    .at(-1)

  return latestMigration ? latestMigration.replace(/\.sql$/, '') : '00_GROUND_TRUTH'
}

function buildSchemaVersionSource(schemaVersion) {
  return [
    '// Generated by scripts/sync-supabase-types.mjs',
    '// Canonical source: BOOTANDSTRAP_WEB supabase/migrations',
    `export const SCHEMA_VERSION = '${schemaVersion}' as const`,
    '',
  ].join('\n')
}

const sourcePath = resolveSourcePath()
const targetPath = path.join(repoRoot, TARGET_TYPES_RELATIVE_PATH)
const schemaVersionPath = path.join(repoRoot, TARGET_SCHEMA_VERSION_RELATIVE_PATH)

const source = normalize(fs.readFileSync(sourcePath, 'utf8'))
const target = fs.existsSync(targetPath)
  ? normalize(fs.readFileSync(targetPath, 'utf8'))
  : null
const schemaVersionSource = buildSchemaVersionSource(resolveSchemaVersion(sourcePath))
const currentSchemaVersionTarget = fs.existsSync(schemaVersionPath)
  ? normalize(fs.readFileSync(schemaVersionPath, 'utf8'))
  : null

if (checkOnly) {
  if (target === source && currentSchemaVersionTarget === schemaVersionSource) {
    process.stdout.write(`Supabase types are in sync: ${TARGET_TYPES_RELATIVE_PATH}\n`)
    process.exit(0)
  }

  process.stderr.write(
    [
      'Supabase types drift detected.',
      `Canonical source: ${sourcePath}`,
      `Target: ${targetPath}`,
      'Run `pnpm sync:supabase-types` with BOOTANDSTRAP_WEB_ROOT or --source-root to resync.',
    ].join('\n') + '\n'
  )
  process.exit(1)
}

fs.mkdirSync(path.dirname(targetPath), { recursive: true })
fs.writeFileSync(targetPath, source, 'utf8')
fs.mkdirSync(path.dirname(schemaVersionPath), { recursive: true })
fs.writeFileSync(schemaVersionPath, schemaVersionSource, 'utf8')
process.stdout.write(`Synced ${TARGET_TYPES_RELATIVE_PATH} from ${sourcePath}\n`)
